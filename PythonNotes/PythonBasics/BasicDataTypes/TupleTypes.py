monday_temperatures = (1, 4, 5)  # A Tuple
print(monday_temperatures)  # (1, 4, 5)


monday_temperatures2 = [1, 4, 5]  # A list
monday_temperatures2.append(6)
print(monday_temperatures2)

monday_temperatures = (1, 4, 5)  # A Tuple
monday_temperatures2.append(6)
print(monday_temperatures)  # (1, 4, 5)  ## Tuples cannot be appended/changed
